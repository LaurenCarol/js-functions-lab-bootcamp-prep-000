function happyHolidaysTo(name) {
  return "Happy holidays, ${name}!"
}
function happyHolidayTo(holiday, yourname) {
  return 'Happy ${holiday}, ${yourname}!'
}
function holidayCountdown(days, holiday) {
  return "It's ${days} days until ${holiday}!"  
}

